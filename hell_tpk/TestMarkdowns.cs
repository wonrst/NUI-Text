import torch.nn as nn

class AutoEncoder(nn.Module):
    def __init__(self):
        super().__init__()
        self.enc = nn.Linear(784, 64)
        self.dec = nn.Linear(64, 784)
    def forward(self, x):
        z = self.enc(x)
        return self.dec(z)
